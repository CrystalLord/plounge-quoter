package org.crystal.ploungequoter

/**
 * The position of the anchor point for some rendered object.
 * The first word indicates the anchor's vertical positioning,
 * the second word indiactes th anchor's horizontal positioning.
 */
enum class Anchor {
    TOP_LEFT,
    TOP_RIGHT,
    BOT_LEFT,
    BOT_RIGHT,
    TOP_CENTER,
    BOT_CENTER,
    CENTER_CENTER,
}
